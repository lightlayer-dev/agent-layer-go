func TestCreateAgUiEmitter_AutoGeneratedIDs(t *testing.T) {
	emitter := CreateAgUiEmitter(func(chunk string) {}, AgUiEmitterOptions{})

	if emitter.ThreadID() == "" {
		t.Error("expected auto-generated thread ID, got empty")
	}
	if emitter.RunID() == "" {
		t.Error("expected auto-generated run ID, got empty")
	}
	if emitter.ThreadID() == emitter.RunID() {
		t.Error("thread ID and run ID should be different")
	}
	// UUID format: 8-4-4-4-12 hex chars
	if len(strings.Split(emitter.ThreadID(), "-")) < 5 {
		t.Errorf("thread ID doesn't look like a UUID: %s", emitter.ThreadID())
	}
}

