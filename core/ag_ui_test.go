func TestEncodeEvents(t *testing.T) {
	events := []AgUiEvent{
		{"type": AgUiRunStarted, "threadId": "t-1"},
		{"type": AgUiRunFinished, "threadId": "t-1"},
	}
	encoded := EncodeEvents(events)

	parts := strings.Split(encoded, "event: ")
	// First element is empty string before first "event: "
	if len(parts) != 3 {
		t.Errorf("expected 3 parts (empty + 2 events), got %d", len(parts))
	}
	if !strings.Contains(parts[1], "RUN_STARTED") {
		t.Errorf("first event should be RUN_STARTED")
	}
	if !strings.Contains(parts[2], "RUN_FINISHED") {
		t.Errorf("second event should be RUN_FINISHED")
	}
}

func TestEncodeEvents_Empty(t *testing.T) {
	encoded := EncodeEvents(nil)
	if encoded != "" {
		t.Errorf("expected empty string for nil events, got: %s", encoded)
	}
}

