func TestEncodeEvent(t *testing.T) {
	event := AgUiEvent{
		"type":    AgUiRunStarted,
		"threadId": "t-1",
		"runId":   "r-1",
	}
	encoded := EncodeEvent(event)

	if !strings.HasPrefix(encoded, "event: RUN_STARTED\ndata: ") {
		t.Errorf("expected SSE format prefix, got: %s", encoded)
	}
	if !strings.HasSuffix(encoded, "\n\n") {
		t.Errorf("expected SSE format to end with double newline, got: %s", encoded)
	}

	// Extract the JSON data portion
	lines := strings.SplitN(encoded, "\n", 3)
	if len(lines) < 2 {
		t.Fatalf("expected at least 2 lines, got %d", len(lines))
	}
	dataLine := strings.TrimPrefix(lines[1], "data: ")
	var parsed map[string]interface{}
	if err := json.Unmarshal([]byte(dataLine), &parsed); err != nil {
		t.Fatalf("data line is not valid JSON: %v", err)
	}
	if parsed["type"] != AgUiRunStarted {
		t.Errorf("expected type RUN_STARTED, got %v", parsed["type"])
	}
	if parsed["threadId"] != "t-1" {
		t.Errorf("expected threadId t-1, got %v", parsed["threadId"])
	}
}

